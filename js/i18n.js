let currentLang = localStorage.getItem('critroll_lang') || 'he';

document.documentElement.lang = currentLang;
document.documentElement.dir = currentLang === 'he' ? 'rtl' : 'ltr';

export function setLanguage(lang) {
    currentLang = lang;
    try { localStorage.setItem('critroll_lang', lang); } catch { /* quota full — lang not persisted */ }
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'he' ? 'rtl' : 'ltr';
    updateDOM();
}

export function getLang() {
    return currentLang;
}

export function t(key) {
    return translations[currentLang][key] || translations['he'][key] || key;
}

export function tg(key, gender) {
    const suffix = gender === 'female' ? '_f' : gender === 'nonbinary' ? '_nb' : '_m';
    return translations[currentLang][key + suffix] || t(key);
}

export function updateDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[currentLang][key]) {
            el.placeholder = translations[currentLang][key];
        }
    });
    document.querySelectorAll('option[data-i18n]').forEach(opt => {
        const key = opt.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            opt.innerText = translations[currentLang][key];
        }
    });
    document.querySelectorAll('[data-i18n-label]').forEach(el => {
        const key = el.getAttribute('data-i18n-label');
        if (translations[currentLang][key]) {
            el.setAttribute('label', translations[currentLang][key]);
        }
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (translations[currentLang][key]) {
            el.setAttribute('title', translations[currentLang][key]);
        }
    });
}
